class MockProductDataSource(ProductDataSource):
    """In-memory data source with sample products.

    Supports filtering by category, price range, and minimum rating.
    """

    def __init__(self, products: list[Product] | None = None) -> None:
        self._products: list[Product] = products if products is not None else _SAMPLE_PRODUCTS
        self._by_id = {p.product_id: p for p in self._products}

    async def search(self, query: str, **filters) -> list[Product]:
        """Return products matching the query and filters."""
        results = list(self._products)

        query = query.strip().lower()
        if query:
            results = [
                p
                for p in results
                if query in p.title.lower() or query in p.category.lower()
            ]

        if category := filters.get("category"):
            category = category.strip().lower().replace(" ", "_")
            results = [p for p in results if p.category == category]

        if price_min := filters.get("price_min"):
            min_price = Decimal(str(price_min))
            results = [p for p in results if p.price >= min_price]

        if price_max := filters.get("price_max"):
            max_price = Decimal(str(price_max))
            results = [p for p in results if p.price <= max_price]

        if rating_min := filters.get("rating_min"):
            min_rating = float(rating_min)
            results = [p for p in results if p.rating is not None and p.rating >= min_rating]

        limit = filters.get("limit")
        if limit is not None:
            results = results[: int(limit)]

        return results

    async def get_by_id(self, product_id: str) -> Product | None:
        """Return a product by ID."""
        return self._by_id.get(product_id)
